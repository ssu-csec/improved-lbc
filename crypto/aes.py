def Xor_Row(row1, row2):
	new_row = []
	for cnt_i in range(4):
		new_row.append(row1[cnt_i] ^ row2[cnt_i])
	return new_row
 
def Rotl(in_row):
	row = copy.deepcopy(in_row)
