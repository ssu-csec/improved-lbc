# block to 4X4 matrix
 
def block2matrix(block):
	matrix = []
	for row in range(4):
		row_list = []
		for col in range(4):
			new_col = block[row*4+col]
			row_list.append(new_col)
		matrix.append(row_list)
	return matrix

def matrix2block(matrix):
	block = []
	for row in matrix:
		block.extend(row)
	return block

def str2hex(string):
	hex_str = []
	for cha in string:
		hex_str.append(ord(cha))
	return hex_str

def hex2str(hex_list):
	str_list = []
	for num in hex_list:
		str_list.append(chr(num))
	return str_list

def ord2chr(ord_matrix):
	output_matrix = []
	for row in ord_matrix:
		out_row = hex2str(row) 
		output_matrix.append(out_row)
	return output_matrix

def chr2ord(chr_matrix):
	output_matrix = []
	for row in chr_matrix:
		out_row = str2hex(row)
		output_matrix.append(out_row)
	return output_matrix

# transpose matrix for steps based on row or column 

def TransMatrix(input_matrix):
	output_matrix = []
	for line in range(4):
		line_list = []
		for segment in range(4):
			line_list.append(input_matrix[segment][line])
		output_matrix.append(line_list)
	return output_matrix


