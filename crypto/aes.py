# rkey[r] = [ [rk00, rk10, rk20, rk 30], ... , [rk03, rk13, rk23, rk33] ]

def key_schedule_Enc(key_matrix):
	rkey = []
	rkey.append(key_matrix)
	for round in range(10):
		new_matrix = []
		new_w0 = Xor_Row(rkey[round][0], KeySR(rkey[round][3], round))
		new_matrix.append(new_w0)
		new_w1 = Xor_Row(rkey[round][1], new_w0)
		new_matrix.append(new_w1)
		new_w2 = Xor_Row(rkey[round][2], new_w1)
		new_matrix.append(new_w2)
		new_w3 = Xor_Row(rkey[round][3], new_w2)
		new_matrix.append(new_w3)
		rkey.append(new_matrix)
	return rkey
 
